package com.demo.core.lab1;

public class Class {
    private int id;
    private String name;
    
    public Class(int id, String name) {
        this.id = id;
        this.name = name;
    }
    
    public int getId() {
        return id;
    }
    
    public String getName() {
        return name;
    }
    
}

